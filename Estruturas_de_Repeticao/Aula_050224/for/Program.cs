// for:
// A estrutura de repetição for é usada quando o número de iterações é conhecido antecipadamente. 
// Ele consiste em três partes: inicialização, condição de continuação e expressão de incremento/decremento.
Console.WriteLine("For (Incremento):");
// inicialização, condição de continuação e expressão de incremento/decremento
for (int contador3 = 0; contador3 < 5; contador3++)
{
    Console.WriteLine(contador3);
}
Console.WriteLine("For (Decremento):");
// inicialização, condição de continuação e expressão de incremento/decremento
for (int contador3 = 3; contador3 >= 0; contador3--)
{
    Console.WriteLine(contador3);
}


for (int contador4 = 0; contador4 <= 1; contador4--)
{
    Console.WriteLine(contador4);
}
Console.WriteLine("For (Decremento):");
// inicialização, condição de continuação e expressão de incremento/decremento
for (int contador4 = 4; contador4 >= 0; contador4--)
{
    Console.WriteLine(contador4);
}